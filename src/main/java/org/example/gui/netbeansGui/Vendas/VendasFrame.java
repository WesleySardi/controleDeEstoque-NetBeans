    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        VendasCadastro vendasCadastro = new VendasCadastro();
        int row = jTable1.getSelectedRow();

        if (row != -1) {
            String cpf = (jTable1.getModel().getValueAt(row, 0).toString());

            vendasCadastro = new VendasCadastro(cpf);
        }

        vendasCadastro.setVisible(true);
        vendasCadastro.pack();
        vendasCadastro.setLocationRelativeTo(null);
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

