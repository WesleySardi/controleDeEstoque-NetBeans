    public void addRowToJTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        List<Vendas> vendas = interactions.visualizarVendas();

        Object rowData[] = new Object[5];

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        
        for (Vendas venda : vendas) {
            rowData[0] = venda.getClienteCpf();
            rowData[1] = venda.getProdutoNome();
            rowData[2] = venda.getQuantidadeVendida();
            rowData[3] = venda.getValorTotal();
            
            // Formatar a data antes de inserir
            Timestamp timestamp = venda.getDataVenda();
            String formattedDate = dateFormat.format(timestamp);
            rowData[4] = formattedDate;

            model.addRow(rowData);
        }
    }

