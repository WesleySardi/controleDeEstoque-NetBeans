    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Integer id2 = null;

        if (!idProduto2.getText().trim().equals("")) {
            id2 = Integer.valueOf(idProduto2.getText());
        }

        if (id2 != null) {
            try {
                Produtos produto = interactions.lerProduto(id2);
                if (produto != null) {
                    interactions.excluirProduto(id2);
                    JOptionPane.showMessageDialog(rootPane, "Produto excluido com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Produto não encontrado: O ID está incorreto.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(ProdutosCadastro.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Erro ao excluir!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Existem campos vazios.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Integer id1 = null;
        Double valor2 = null;
        Integer qtd2 = null;

        String nome2 = nomeProduto2.getText();
        String desc2 = descricaoProduto2.getText();

        if (!idProduto1.getText().trim().equals("")) {
            id1 = Integer.valueOf(idProduto1.getText());
        }

        if (!valorProduto2.getText().trim().equals("")) {
            valor2 = Double.valueOf(valorProduto2.getText());
        }

        if (!quantidadeProduto2.getText().trim().equals("")) {
            qtd2 = Integer.valueOf(quantidadeProduto2.getText());
        }

        if (id1 != null && valor2 != null && qtd2 != null && !nome2.trim().equals("") && !desc2.trim().equals("")) {
            Produtos produtos = new Produtos(id1, valor2, qtd2, nome2, desc2);
            try {
                Produtos produtosExists = interactions.lerProduto(id1);

                if (produtosExists != null) {
                    try {
                        if (qtd2 < 0) {
                            throw new IllegalArgumentException("Quantidade não pode ser menor que 0.");
                        }
                        interactions.atualizarProduto(produtos);
                        JOptionPane.showMessageDialog(rootPane, "Produto atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                    } catch (IllegalArgumentException ex) {
                        JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    throw new IllegalArgumentException("Produto não encontrado: O ID está incorreto.");
                }

            } catch (SQLException ex) {
                Logger.getLogger(ProdutosCadastro.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Valor inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
            } catch (IllegalArgumentException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Existem campos vazios.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

