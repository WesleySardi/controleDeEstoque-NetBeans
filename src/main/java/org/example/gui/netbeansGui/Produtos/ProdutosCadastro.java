    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome1 = nomeProduto1.getText();
        String desc1 = descricaoProduto1.getText();
        Double valor1 = null;
        Integer qtd1 = null;

        if (!valorProduto1.getText().trim().equals("")) {
            valor1 = Double.valueOf(valorProduto1.getText());
        }

        if (!quantidadeProduto1.getText().trim().equals("")) {
            qtd1 = Integer.valueOf(quantidadeProduto1.getText());
        }

        if (valor1 != null && qtd1 != null && !nome1.trim().equals("") && !desc1.trim().equals("")) {
            Produtos produtos = new Produtos(valor1, qtd1, nome1, desc1);

            try {
                interactions.criarProduto(produtos);
                JOptionPane.showMessageDialog(rootPane, "Produto cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                Logger.getLogger(ProdutosCadastro.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Valor inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Existem campos vazios.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

